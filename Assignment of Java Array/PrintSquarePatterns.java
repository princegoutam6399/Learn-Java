class PrintSquarePatterns {
    public static void main(String[] args) {
        int rows = 8;
		int col = 5;
        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < col; j++) {
                System.out.print(" * ");
            }
            System.out.println(); 
        }
    }
}
